package com.luswei.terminal.model;

public class ResponseMessage {
	
	private String head;
	private Object body;
	
	public String getHead() {
		return head;
	}
	public void setHead(String head) {
		this.head = head;
	}
	public Object getBody() {
		return body;
	}
	public void setBody(Object body) {
		this.body = body;
	}
	
	

}
